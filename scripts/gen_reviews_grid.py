def card_html(r, index):
    name, meta, text, full, card_cls, text_cls, photos = r
    cls = f"review-card {card_cls}".strip()
    av = name[0].upper()
    return f"""              <article class="{cls}" data-index="{index}" itemscope itemtype="https://schema.org/Review">
                <div class="rc-top">
                  <div class="rc-avatar" aria-hidden="true">{av}</div>
                  <div>
                    <div class="rc-name" itemprop="author">{name}</div>
                    <div class="rc-meta">{meta}</div>
                  </div>
                </div>
                {STARS}
                <p class="{text_cls}" itemprop="reviewBody">{text}</p>
              </article>
"""


def build_grid():
    cols = [REVIEWS[0:3], REVIEWS[3:6], REVIEWS[6:9]]
    lines = ['        <div class="reviews-grid" id="reviewsGrid">']
    index = 0
    for col in cols:
        lines.append('          <div class="col">')
        for r in col:
            lines.append(card_html(r, index))
            index += 1
        lines.append('          </div>')
    lines.append('        </div>')
    lines.append('        <div class="reviews-mobile" id="reviewsMobile"></div>')
    return "\n".join(lines)


def main():
    root = Path(__file__).resolve().parents[1]
    html_path = root / "reviews.html"
    content = html_path.read_text(encoding="utf-8")
    grid = build_grid()
    pattern = r'        <div class="reviews-grid" id="reviewsGrid">.*?<div class="reviews-mobile" id="reviewsMobile"></div>'
    new_content, n = re.subn(pattern, grid, content, count=1, flags=re.DOTALL)
    if n != 1:
        raise SystemExit(f"Replace failed: {n} matches")
    html_path.write_text(new_content, encoding="utf-8")
    print("Updated reviews.html grid")


if __name__ == "__main__":
    main()
