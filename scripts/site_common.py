def service_href(prefix, slug):
    return f"{prefix}services/{slug}.html"


def render_nav_dropdown(prefix=""):
    from services_data import CATEGORIES

    cats = []
    for cat in CATEGORIES:
        items = "\n".join(
            f'                <li><a href="{service_href(prefix, s["slug"])}" class="nav__dropdown-link">{s["title"]}</a></li>'
            for s in cat["services"]
        )
        cats.append(f"""          <li class="nav__dropdown-cat">
            <button type="button" class="nav__dropdown-cat-btn" aria-expanded="false">{cat["name"]}{DROPDOWN_ICON}</button>
            <ul class="nav__dropdown-sub">
{items}
            </ul>
          </li>""")
    cats_html = "\n".join(cats)
    all_link = f'{prefix}services.html'
    return f"""        <div class="nav__item nav__dropdown" id="navServices">
          <button type="button" class="nav__link nav__dropdown-trigger" aria-expanded="false" aria-haspopup="true">
            Услуги{DROPDOWN_ICON}
          </button>
          <div class="nav__dropdown-panel" hidden>
            <div class="nav__dropdown-panel-inner">
              <a href="{all_link}" class="nav__dropdown-all">Все услуги →</a>
              <ul class="nav__dropdown-cats">
{cats_html}
              </ul>
            </div>
          </div>
        </div>"""


def render_nav(prefix="", active=None):
    parts = []
    for href, label, key in NAV_PAGES:
        if key == "services":
            services_dropdown = render_nav_dropdown(prefix)
            if active == "services":
                services_dropdown = services_dropdown.replace(
                    'class="nav__link nav__dropdown-trigger"',
                    'class="nav__link nav__dropdown-trigger nav__link--active"',
                )
            parts.append(services_dropdown)
        else:
            cls = "nav__link"
            if active == key:
                cls += " nav__link--active"
            parts.append(f'        <a href="{prefix}{href}" class="{cls}">{label}</a>')
    return "\n".join(parts)


